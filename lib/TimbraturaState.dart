class TimbraturaState {
  int id;
  int user_id;
  String sede;
  String datetime;
  String approvata;
  String verso;
  int getId() {
    return id;
  }
  void setId(int id) {
    this.id = id;
  }
  int getUser_id() {
    return user_id;
  }
  void setUser_id(int user_id) {
    this.user_id = user_id;
  }
  String getSede() {
    return sede;
  }
  void setSede(String sede) {
    this.sede = sede;
  }
  String getDatetime() {
    return datetime;
  }
  void setDatetime(String datetime) {
    this.datetime = datetime;
  }
  String getApprovata() {
    return approvata;
  }
  void setApprovata(String approvata) {
    this.approvata = approvata;
  }
  String getVerso() {
    return verso;
  }
  void setVerso(String verso) {
    this.verso = verso;
  }
}
